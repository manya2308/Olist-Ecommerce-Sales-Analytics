--Which states experience the longest delivery delays?
SELECT
    c.customer_state,
    AVG(
        DATEDIFF(
            DAY,
            o.order_estimated_delivery_date,
            o.order_delivered_customer_date
        ) * 1.0
    ) AS avg_delivery_delay
FROM olist_orders_dataset1 o
INNER JOIN olist_customers_dataset c
    ON o.customer_id = c.customer_id
WHERE o.order_delivered_customer_date IS NOT NULL
GROUP BY c.customer_state
ORDER BY avg_delivery_delay DESC;

--INTERPRETATION
/*The marketplace is consistently outperforming its promised delivery timelines across all states.
Customers receive their orders approximately 11–21 days earlier than expected on average.
States such as MS and SP have the smallest lead times (around 11 days early), while states such as AC and RO receive orders nearly 20 days
before the estimated delivery date.
*/

