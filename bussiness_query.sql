--Which sellers are responsible for the highest number of delayed deliveries?
SELECT TOP 10
    oi.seller_id,
    COUNT(*) AS delayed_orders
FROM olist_order_items_dataset oi
INNER JOIN olist_orders_dataset1 o
    ON oi.order_id = o.order_id
WHERE o.order_delivered_customer_date >
      o.order_estimated_delivery_date
GROUP BY oi.seller_id
ORDER BY delayed_orders DESC;


WITH seller_delays AS
(
    SELECT
        oi.seller_id,
        COUNT(*) AS total_orders,
        SUM(
            CASE
                WHEN o.order_delivered_customer_date >
                     o.order_estimated_delivery_date
                THEN 1
                ELSE 0
            END
        ) AS delayed_orders
    FROM olist_order_items_dataset oi
    INNER JOIN olist_orders_dataset1 o
        ON oi.order_id = o.order_id
    GROUP BY oi.seller_id
)
SELECT
    seller_id,
    total_orders,
    delayed_orders,
    ROUND(
        100.0 * delayed_orders / total_orders,
        2
    ) AS delay_percentage
FROM seller_delays
WHERE delayed_orders > 0
ORDER BY delayed_orders DESC;

--INTERPRETATION
/* While some sellers contribute a large number of delayed deliveries due to their high order volume,
others exhibit significantly higher delay rates. Sellers with high delay percentages should be prioritized for 
operational review, as they pose a greater risk to customer satisfaction and service quality.
*/

--How does delivery time affect customer satisfaction?

WITH delivery_analysis AS
(
    SELECT
        CASE
            WHEN DATEDIFF(DAY, o.order_purchase_timestamp,
                          o.order_delivered_customer_date) <= 7
                THEN '0-7 Days'

            WHEN DATEDIFF(DAY, o.order_purchase_timestamp,
                          o.order_delivered_customer_date) <= 14
                THEN '8-14 Days'

            WHEN DATEDIFF(DAY, o.order_purchase_timestamp,
                          o.order_delivered_customer_date) <= 21
                THEN '15-21 Days'

            ELSE '21+ Days'
        END AS delivery_bucket,
        r.review_score
    FROM olist_orders_dataset1 o
    INNER JOIN olist_order_reviews_dataset1 r
        ON o.order_id = r.order_id
    WHERE o.order_delivered_customer_date IS NOT NULL
)
SELECT
    delivery_bucket,
    AVG(CAST(review_score AS FLOAT)) AS avg_rating,
    COUNT(*) AS orders
FROM delivery_analysis
GROUP BY delivery_bucket
ORDER BY delivery_bucket;

--INTERPRETATION
/*Customer satisfaction is strongly influenced by delivery speed. Orders delivered within 7 days 
receive the highest average rating (4.41), while orders taking more than 21 days receive a significantly lower average rating (3.06). 
This indicates that customers value fast delivery and become less satisfied as delivery times increase.
*/

--Which product categories generate high revenue but consistently low review scores?
WITH category_performance AS
(
    SELECT
        p.product_category_name,
        SUM(oi.price + oi.freight_value) AS revenue,
        AVG(CAST(r.review_score AS FLOAT)) AS avg_rating
    FROM olist_products_dataset p
    INNER JOIN olist_order_items_dataset oi
        ON p.product_id = oi.product_id
    INNER JOIN olist_orders_dataset1 o
        ON oi.order_id = o.order_id
    INNER JOIN olist_order_reviews_dataset1 r
        ON o.order_id = r.order_id
    GROUP BY p.product_category_name
)
SELECT *
FROM category_performance
WHERE avg_rating < 4
ORDER BY revenue DESC;

--INTERPRETATION
/*These categories are commercially successful and attract substantial customer demand, 
but customer satisfaction does not match their revenue performance. This suggests potential issues related to 
product quality, delivery experience, product expectations, packaging, or after-sales service.
*/

--Which products should be investigated due to poor ratings despite strong sales?
SELECT
    p.product_id,
    p.product_category_name,
    SUM(oi.price + oi.freight_value) AS revenue,
    AVG(CAST(r.review_score AS FLOAT)) AS avg_rating
FROM olist_products_dataset p
INNER JOIN olist_order_items_dataset oi
    ON p.product_id = oi.product_id
INNER JOIN olist_order_reviews_dataset1 r
    ON oi.order_id = r.order_id
GROUP BY
    p.product_id,
    p.product_category_name
HAVING
    SUM(oi.price + oi.freight_value) > 10000
    AND AVG(CAST(r.review_score AS FLOAT)) < 4
ORDER BY revenue DESC;

