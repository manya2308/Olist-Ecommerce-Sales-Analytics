--Which sellers have declining sales performance over time?
WITH monthly_seller_sales AS
(
    SELECT
        oi.seller_id,
        FORMAT(o.order_purchase_timestamp,'yyyy-MM') AS sales_month,
        SUM(oi.price + oi.freight_value) AS revenue
    FROM olist_order_items_dataset oi
    INNER JOIN olist_orders_dataset1 o
        ON oi.order_id = o.order_id
    GROUP BY
        oi.seller_id,
        FORMAT(o.order_purchase_timestamp,'yyyy-MM')
),

seller_trend AS
(
    SELECT
        seller_id,
        sales_month,
        revenue,
        LAG(revenue) OVER
        (
            PARTITION BY seller_id
            ORDER BY sales_month
        ) AS previous_revenue
    FROM monthly_seller_sales
)
SELECT *
FROM seller_trend
WHERE revenue < previous_revenue
ORDER BY seller_id,sales_month;

--INTERPRETATION
/*Analysis of monthly seller revenue identified several sellers with declining sales performance over time. 
The decline suggests potential challenges related to market demand, competition, inventory availability, or customer engagement.
Sellers experiencing the largest revenue contractions should be prioritized for further investigation to prevent future revenue loss and improve marketplace performance.
*/

