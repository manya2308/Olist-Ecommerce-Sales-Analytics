--Who are the top 10% of customers contributing the most revenue?
WITH customer_revenue AS
(   SELECT
        c.customer_id,
        SUM(od.price + od.freight_value) AS revenue
    FROM olist_customers_dataset c
    INNER JOIN olist_orders_dataset1 o
        ON c.customer_id = o.customer_id
    INNER JOIN olist_order_items_dataset od
        ON o.order_id = od.order_id
    GROUP BY c.customer_id
),
customer_segments AS
( SELECT
        customer_id,
        revenue,
        NTILE(10) OVER (ORDER BY revenue DESC) AS revenue_decile
    FROM customer_revenue)
SELECT *
FROM customer_segments
WHERE revenue_decile = 1
ORDER BY revenue DESC;
--INTERPRETATION
/*The top 10% of customers generated substantially higher revenue than the remaining customer base.
This indicates that a relatively small segment of customers contributes a disproportionate share of total business revenue,
highlighting the importance of retaining and engaging high-value customers.
*/

--What percentage of revenue comes from repeat customers versus one-time customers?
WITH customer_orders AS
(
    SELECT
        c.customer_id,
        COUNT(DISTINCT o.order_id) AS total_orders,
        SUM(od.price + od.freight_value) AS revenue
    FROM olist_customers_dataset c
    INNER JOIN olist_orders_dataset1 o
        ON c.customer_id = o.customer_id
    INNER JOIN olist_order_items_dataset od
        ON o.order_id = od.order_id
    GROUP BY c.customer_id
),
customer_type AS
(
    SELECT
        CASE
            WHEN total_orders = 1 THEN 'One-Time Customer'
            ELSE 'Repeat Customer'
        END AS customer_segment,
        revenue
    FROM customer_orders)
SELECT
    customer_segment,
    SUM(revenue) AS total_revenue,
    ROUND(
        100.0 * SUM(revenue) /
        SUM(SUM(revenue)) OVER (),
        2
    ) AS revenue_percentage
FROM customer_type
GROUP BY customer_segment;
--INTERPRETATION
/*Analysis based on customer_id indicates that 100% of revenue was generated by one-time customers.
However, this result is likely influenced by the dataset's customer identification structure,
where each purchase may be assigned a unique customer_id. 
Further validation using customer_unique_id is required to accurately distinguish repeat customers from one-time customers.
*/
-- Which customer segments should be targeted for retention campaigns?
WITH customer_revenue AS
(
    SELECT
        c.customer_id,
        SUM(od.price + od.freight_value) AS revenue
    FROM olist_customers_dataset c
    INNER JOIN olist_orders_dataset1 o
        ON c.customer_id = o.customer_id
    INNER JOIN olist_order_items_dataset od
        ON o.order_id = od.order_id
    GROUP BY c.customer_id
),
customer_segments AS
(
    SELECT
        customer_id,
        revenue,
        NTILE(4) OVER (ORDER BY revenue DESC) AS segment
    FROM customer_revenue
)

SELECT
    CASE
        WHEN segment = 1 THEN 'High Value'
        WHEN segment IN (2,3) THEN 'Medium Value'
        WHEN segment = 4 THEN 'Low Value'
    END AS customer_segment,
    COUNT(*) AS customers,
    SUM(revenue) AS total_revenue,
    ROUND(
        100.0 * SUM(revenue) / SUM(SUM(revenue)) OVER (),
        2
    ) AS revenue_percentage
FROM customer_segments
GROUP BY
    CASE
        WHEN segment = 1 THEN 'High Value'
        WHEN segment IN (2,3) THEN 'Medium Value'
        WHEN segment = 4 THEN 'Low Value'
    END
ORDER BY total_revenue DESC;

--INTERPRETATION
/*Revenue is heavily concentrated among High Value customers.
Although they represent a relatively small portion of the customer base, 
they generate the majority of the company's revenue. 
This indicates that customer value is unevenly distributed and that retaining High Value customers is critical to sustaining business performance.
*/

--Which regions have high customer acquisition but low repeat purchases?
WITH customer_orders AS
(
    SELECT
        c.customer_unique_id,
        c.customer_state,
        COUNT(DISTINCT o.order_id) AS total_orders
    FROM olist_customers_dataset c
    INNER JOIN olist_orders_dataset1 o
        ON c.customer_id = o.customer_id
    GROUP BY
        c.customer_unique_id,
        c.customer_state
),
state_metrics AS
(
    SELECT
        customer_state,
        COUNT(*) AS total_customers,
        SUM(
            CASE
                WHEN total_orders > 1 THEN 1
                ELSE 0
            END
        ) AS repeat_customers
    FROM customer_orders
    GROUP BY customer_state
)

SELECT
    customer_state,
    total_customers,
    repeat_customers,
    ROUND(
        100.0 * repeat_customers / total_customers,
        2
    ) AS repeat_purchase_rate
FROM state_metrics
ORDER BY total_customers DESC,
         repeat_purchase_rate ASC;
--INTERPRETATION
/*While São Paulo contributes the largest customer base (40,302 customers), only 3.22% of customers make repeat purchases. 
Similar patterns are observed across other major states, suggesting that customer acquisition is strong but retention remains a challenge. 
Improving customer loyalty in these high-volume regions could generate substantial long-term revenue growth without increasing acquisition costs.
*/

--What is the average time between purchases for repeat customers?
WITH customer_orders AS
(
    SELECT
        c.customer_unique_id,
        o.order_purchase_timestamp
    FROM olist_customers_dataset c
    INNER JOIN olist_orders_dataset1 o
        ON c.customer_id = o.customer_id
),

purchase_gaps AS
(
    SELECT
        customer_unique_id,
        order_purchase_timestamp,
        LAG(order_purchase_timestamp) OVER
        (
            PARTITION BY customer_unique_id
            ORDER BY order_purchase_timestamp
        ) AS previous_purchase
    FROM customer_orders
)

SELECT
    AVG(
        DATEDIFF(
            DAY,
            previous_purchase,
            order_purchase_timestamp
        ) * 1.0
    ) AS avg_days_between_purchases
FROM purchase_gaps
WHERE previous_purchase IS NOT NULL;

--INTERPRETATION
/*Analysis of repeat purchasing behavior revealed that customers take an average of 78.23 days to place their next order.
This indicates a repurchase cycle of roughly two and a half months. 
Understanding this purchasing cadence can help the business optimize the timing of retention campaigns and promotional offers to increase repeat purchase rates.
*/

